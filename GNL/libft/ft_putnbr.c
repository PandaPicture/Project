#include "libft.h"

void	ft_putnbr(int n)
{
	int min;

	min = -2147483648;
	if (n == min)
		ft_putstr("-2147483648");
	if (n < 0 && n > min)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n < 10 && n > min)
		ft_putchar(n + 48);
	else if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
