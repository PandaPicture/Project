#include "libft.h"

char	*ft_strlow(char *s)
{
	int var;

	var = 0;
	while (s[var])
	{
		if (s[var] >= 'A' && s[var] <= 'Z')
			s[var] += 32;
		var++;
	}
	return (s);
}
